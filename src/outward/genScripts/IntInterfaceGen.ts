export class IntInterfaceGen {
  public static className: string;
  public static InterfaceName: string;

  public static init0(): void {
    IntInterfaceGen.className = "IntInterfaceGen";
    IntInterfaceGen.InterfaceName = "Int";
  }

  public static get(): string {
    let methodDeclarations = "";
    const fws = Library.getAllIntMethodFWs();

    for(let i = 0; i < fws.length; i++) {
      const fw = fws[i];
      methodDeclarations += `  ${fw.name}${TypeStr.function(fw, ":")};`;

      if (i !== fws.length - 1) {
        methodDeclarations += "\n";
      }
    }

    return `interface ${IntInterfaceGen.InterfaceName} {\n${methodDeclarations}\n}`
  }


  public static dependencies(): Set<Class> {
    return new Set([
      FunctionWrapper, Library, Init, TypeStr
    ]);
  }
}


